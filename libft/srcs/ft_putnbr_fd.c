#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n < 10 && n >= 0)
		ft_putchar_fd(n + 48, fd);
	else
	{
		ft_putnbr_fd(((n / 10 < 0) ? -(n / 10) : n / 10), fd);
		ft_putnbr_fd(((n % 10 < 0) ? -(n % 10) : n % 10), fd);
	}
}
